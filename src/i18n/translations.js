export function resolveLanguage(value) {
  return value === 'vi' ? 'vi' : 'en';
}

export function getTranslation(language, path) {
  const keys = Array.isArray(path) ? path : String(path).split('.');

  const lookup = (source) => keys.reduce((value, key) => (value && value[key] !== undefined ? value[key] : undefined), source);

  return lookup(translations[language]) ?? lookup(translations[defaultLanguage]) ?? path;
}
